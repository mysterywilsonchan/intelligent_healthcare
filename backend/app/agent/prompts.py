def build_decision_user_prompt(context: dict) -> str:
    """把结构化证据拼成给大模型的 user 文本。"""
    import json

    return (
        "以下是该老人的结构化健康证据（JSON），请据此完成综合评估：\n\n"
        + json.dumps(context, ensure_ascii=False, indent=2)
    )
